import assert from 'node:assert/strict';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { documentRestoreProfileV1074, restorePixelV1074 } from '../source/src/modules/scan/documentRestoreEngineV1074.js';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const read = relative => fs.readFileSync(path.join(ROOT, relative), 'utf8');
const pass = (condition, label) => {
  assert.ok(condition, label);
  console.log(`PASS ${label}`);
};

const shadowPaper = restorePixelV1074({ red:132, green:126, blue:116, background:130, soft:128, mode:'color' });
pass(shadowPaper.value >= 238, 'deep paper shadow is normalized toward clean white');
pass(Math.max(shadowPaper.red, shadowPaper.green, shadowPaper.blue) <= 255, 'paper whitening stays inside the valid channel range');

const printedLine = restorePixelV1074({ red:45, green:44, blue:42, background:205, soft:128, mode:'gray' });
pass(printedLine.value <= 45, 'dark printed text is strengthened after shadow normalization');

const faintPencil = restorePixelV1074({ red:122, green:120, blue:116, background:198, soft:154, mode:'gray' });
pass(faintPencil.value < 150, 'faint handwriting remains visible instead of being erased');

const blueInk = restorePixelV1074({ red:56, green:88, blue:158, background:205, soft:132, mode:'color' });
pass(blueInk.blue > blueInk.red + 25, 'blue handwriting keeps its color evidence');
pass(blueInk.blue > blueInk.green, 'colored signatures are not flattened into grayscale');

const neutralStain = restorePixelV1074({ red:178, green:166, blue:151, background:174, soft:170, mode:'color' });
pass(neutralStain.value >= 235, 'low-detail paper discoloration is suppressed');

const binaryInk = restorePixelV1074({ red:74, green:73, blue:70, background:210, soft:130, mode:'bw' });
const binaryPaper = restorePixelV1074({ red:190, green:188, blue:184, background:190, soft:188, mode:'bw' });
pass(binaryInk.value === 0, 'high-contrast OCR keeps dark ink');
pass(binaryPaper.value === 255, 'high-contrast OCR cleans paper background');

const profile = documentRestoreProfileV1074();
pass(profile.networkRequired === false, 'Document Restore runs locally without a network model');
pass(profile.generative === false, 'Document Restore preserves evidence and does not synthesize document content');
pass(profile.handwritingPreservation === 'chroma-aware', 'Document Restore explicitly preserves colored handwriting');

const restoreSource = read('source/src/modules/scan/documentRestoreEngineV1074.js');
pass(restoreSource.includes('multi-scale-illumination'), 'restoration profile records multi-scale illumination removal');
pass(restoreSource.includes('whiteBalanceV1074'), 'paper color cast is corrected before enhancement');
pass(restoreSource.includes('borderWhitenV1074'), 'residual paper-edge discoloration is cleaned');
pass(!restoreSource.includes('fetch(') && !restoreSource.includes('http://') && !restoreSource.includes('https://'), 'restore engine has no remote dependency');
pass(!/name|address|license|vin|credit card/i.test(restoreSource), 'no user document contents are stored in the code fixture');

const scanner = read('source/src/modules/scan/documentScannerEngine.js');
pass(scanner.includes('document-restore-v1074'), 'production scanner imports Document Restore v107.4');
pass(scanner.includes('restoreDocumentCanvasV1074(canvas, mode'), 'all non-original variants use the restoration engine');

const adapter = read('source/src/modules/scan/webScannerAdapterV106.js');
pass(adapter.includes('road-ready-document-restore-'), 'driver-visible enhanced color is generated by Document Restore');
pass(adapter.includes("original:createVersionRecordV106('original'"), 'immutable original preservation remains active');
pass(adapter.includes("ocrSelected:createVersionRecordV106('ocr-selected'"), 'OCR-selected evidence remains separate from the visible clean image');

const capture = read('source/src/modules/scan/SmartDocumentCaptureV106.jsx');
pass(capture.includes('driverPreviewFileV1072'), 'driver preview continues to prefer the enhanced color version');
pass(capture.includes('safePreviewUrlV1073'), 'Safari preview URL integrity remains installed');

const chain = read('scripts/run-v106-smart-capture.mjs');
pass(chain.includes('patch-v1074-document-restore.mjs'), 'release chain installs Document Restore v107.4');
pass(chain.includes('finalize-v1074-document-restore.mjs'), 'release chain pins Document Restore v107.4 metadata');

console.log('PASS — v107.4 Document Restore regression suite');
